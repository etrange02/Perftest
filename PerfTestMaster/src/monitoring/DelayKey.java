package monitoring;

public class DelayKey {

    private int delay;



    /* *********************************************************************
     * CONSTRUCTORS ********************************************************
     * *********************************************************************/

    public DelayKey(int delay) {
	this.delay = delay;
    }



    /* *********************************************************************
     * GETTERS/SETTERS *****************************************************
     * *********************************************************************/

    public double getDelay() {
	return delay;
    }



    /* *********************************************************************
     * OTHERS **************************************************************
     * *********************************************************************/

    @Override
    public boolean equals(Object object) {
	return (object instanceof DelayKey) &&
		((DelayKey)object).getDelay()==delay;
    }
}
